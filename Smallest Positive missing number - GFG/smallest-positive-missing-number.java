class Solution
{
    //Function to find the smallest positive number missing from the array.
    static int missingNumber(int arr[], int size)
    {
        // bhai dekh k=0 s chalta hu akhir tk chalata hu n tk jaha 0 se n tk mila
        // wha n+1 wrna last wala n
        Arrays.sort(arr);
        int k=1;
        for (int i=0;i<arr.length;i++ ){ 
         if(arr[i]==k){ 
          k++;   
         }
        }
        return  k;
    }
}


